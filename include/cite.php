<h1 class="text-center">Cite</h1>
<p class="lead magick-description">Learn how to cite ImageMagick in your work.  Choose from these citation formats:</p>

<h5>Citation in APA style</h5>
<ul><pre class="bg-light"><samp>The ImageMagick Development Team. (2021). ImageMagick. Retrieved from https://imagemagick.org</samp></pre></ul>

<h5>Citation in Vancouver style</h5>
<ul><pre class="bg-light"><samp>1. The ImageMagick Development Team. ImageMagick [Internet]. 2021. Available from: https://imagemagick.org</samp></pre></ul>

<h5>Citation in Harvard style</h5>
<ul><pre class="bg-light"><sampl>The ImageMagick Development Team, 2021. ImageMagick, Available at: https://imagemagick.org.</samp></pre></ul>
<h5>Citation in Bibtex format</h5>
<ul><pre class="bg-light"><samp>@software{imagemagick,
  author = {{The ImageMagick Development Team}},
  title = {ImageMagick},
  url = {https://imagemagick.org},
  version = {7.0.10},
  date = {2021-01-04},
}</samp></pre></ul>
