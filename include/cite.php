<h1 class="text-center">Cite</h1>
<p class="lead magick-description">Learn how to cite ImageMagick in your work.  Choose from these citation formats:</p>

<h4>Citation in APA style</h4>
<pre class="bg-light text-dark mx-4"><samp>The ImageMagick Development Team. (2021). ImageMagick. Retrieved from https://imagemagick.org</samp></pre>

<h4>Citation in Vancouver style</h4>
<pre class="bg-light text-dark mx-4"><samp>1. The ImageMagick Development Team. ImageMagick [Internet]. 2021. Available from: https://imagemagick.org</samp></pre>

<h4>Citation in Harvard style</h4>
<pre class="bg-light text-dark mx-4"><sampl>The ImageMagick Development Team, 2021. ImageMagick, Available at: https://imagemagick.org.</samp></pre>
<h4>Citation in Bibtex format</h4>
<pre class="bg-light text-dark mx-4"><samp>@software{imagemagick,
  author = {{The ImageMagick Development Team}},
  title = {ImageMagick},
  url = {https://imagemagick.org},
  version = {7.0.10},
  date = {2021-01-04},
}</samp></pre>
